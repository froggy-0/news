-- =============================================================================
-- 1. 신호 기록
-- 작성: scripts/fill_signal_outcomes.py, src/morning_brief/signal_logger.py
-- RLS: ON (정책 미확인)
-- =============================================================================

CREATE TABLE IF NOT EXISTS signal_log (
    id               BIGINT      GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    signal_date      DATE        NOT NULL UNIQUE,
    regime_state     TEXT        NOT NULL,   -- BullQuiet / BullHeated / BearPanic / Choppy / Transitional
    vol_level        TEXT        NOT NULL,   -- High / Mid / Low
    vol_trend        TEXT,                   -- rising / falling / stable
    overlay_decision TEXT        NOT NULL,   -- promote / research_only
    confidence       TEXT,                   -- HIGH / MEDIUM / null
    reasons          JSONB,                  -- ["vol_regime_v2_promoted", ...]
    btc_price_open   NUMERIC,
    btc_price_7d     NUMERIC,               -- fill_signal_outcomes.py 자동 채움
    ret_7d           NUMERIC,               -- (price_7d / price_open) - 1
    hit              BOOLEAN,
    created_at       TIMESTAMPTZ DEFAULT NOW()
);


-- =============================================================================
-- 2. 구독 관리
-- 작성: frontend/lib/subscriptions/repository.ts (Cloudflare Pages Functions)
-- 읽기: src/morning_brief/subscriptions/supabase_repository.py (브리핑 발송)
-- RLS: ON (정책 미확인)
-- =============================================================================

CREATE TABLE IF NOT EXISTS subscriptions (
    id               UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
    email            TEXT        NOT NULL,
    email_normalized TEXT        NOT NULL,
    newsletter       TEXT        NOT NULL,   -- 'morning-brief'
    status           TEXT        NOT NULL,   -- pending / active / unsubscribed / bounced
    status_reason    TEXT,                   -- awaiting_confirmation / confirmed / user_unsubscribed
    subscribed_at    TIMESTAMPTZ,
    unsubscribed_at  TIMESTAMPTZ,
    bounced_at       TIMESTAMPTZ,
    created_at       TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at       TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE UNIQUE INDEX subscriptions_newsletter_email_normalized_idx
    ON subscriptions (newsletter, email_normalized);

CREATE INDEX subscriptions_status_idx
    ON subscriptions (newsletter, status);   -- list_active_recipients 쿼리용


CREATE TABLE IF NOT EXISTS subscription_tokens (
    id              UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
    subscriber_id   UUID        NOT NULL REFERENCES subscriptions(id) ON DELETE CASCADE,
    token_type      TEXT        NOT NULL,   -- 'confirm_subscription'
    token_hash      TEXT        NOT NULL UNIQUE,
    expires_at      TIMESTAMPTZ NOT NULL,
    consumed_at     TIMESTAMPTZ,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX subscription_tokens_lookup_idx
    ON subscription_tokens (token_type, token_hash);   -- findConfirmationToken 쿼리용


-- =============================================================================
