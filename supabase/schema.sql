-- =============================================================================
-- 6. BTC ETF 데이터 레이어
-- 작성: src/morning_brief/data/etf_storage.py
-- Silver:    필드별 정규화 레코드 (ELT)
-- Gold:      티커별 집계 레코드 (공식 소스), PK = (ticker, as_of_date)
-- Reference: 집계 레코드 (aggregator / reference_only), UNIQUE = (ticker, as_of_date, source_type), PK 없음
-- Bronze:    Supabase Storage 버킷 'btc-etf-bronze' (테이블 아님)
--            경로 패턴: {ticker}/{YYYYMMDD}/{run_id}-{checksum[:12]}.{format}
-- RLS: OFF (3개 테이블 모두)
-- =============================================================================

-- 6-1. btc_etf_silver — PK 없음, UNIQUE = (ticker, as_of_date, field_name, source_type)
CREATE TABLE IF NOT EXISTS btc_etf_silver (
    run_id             TEXT        NOT NULL,
    ticker             TEXT        NOT NULL,
    issuer             TEXT        NOT NULL,
    field_name         TEXT        NOT NULL,
    field_value        JSONB,
    value_type         TEXT        NOT NULL,   -- null / bool / int / float / str
    unit               TEXT,
    as_of_date         DATE        NOT NULL,
    collected_at       TIMESTAMPTZ NOT NULL,
    source_url         TEXT        NOT NULL,
    source_type        TEXT        NOT NULL,   -- official_html / official_json / official_csv / aggregator
    source_format      TEXT        NOT NULL,
    parse_method       TEXT        NOT NULL,
    source_file_url    TEXT,
    quality_status     TEXT        NOT NULL,   -- ok / degraded / critical
    raw_label          TEXT,
    raw_text           TEXT,
    schema_version     TEXT        NOT NULL DEFAULT 'v1',
    bronze_object_path TEXT
);

CREATE UNIQUE INDEX btc_etf_silver_unique
    ON btc_etf_silver (ticker, as_of_date, field_name, source_type);

CREATE INDEX idx_btc_etf_silver_as_of_date
    ON btc_etf_silver (as_of_date);

CREATE INDEX idx_btc_etf_silver_ticker_as_of_date
    ON btc_etf_silver (ticker, as_of_date);


